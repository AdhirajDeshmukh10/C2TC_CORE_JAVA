package com.cap.java;

public class AssignmentOperator {

	public static void main(String[] args)
	{
		//Variable Declaration
		int x = 4;
		
		//Arithmetic Operation
		x += 5;  x = x + 5;
		
		x -= 5;  x = x - 5;
		
		x *= 5;  x = x * 5;
		
		x /= 4;  x = x / 4;
		
		x %= 5;  x = x % 5;
		
		System.out.println("Final value of x after arithmetic operation = "+x);

	}

}
